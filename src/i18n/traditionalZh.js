export function toTraditionalZh(text) {
  if (!text || typeof text !== 'string') return text;
  let out = text;
  for (const [from, to] of PAIRS) {
    out = out.split(from).join(to);
  }
  return out;
}

export function deepToTraditional(obj) {
  if (typeof obj === 'string') return toTraditionalZh(obj);
  if (Array.isArray(obj)) return obj.map(deepToTraditional);
  if (obj && typeof obj === 'object') {
    return Object.fromEntries(
      Object.entries(obj).map(([k, v]) => [k, deepToTraditional(v)])
    );
  }
  return obj;
}
